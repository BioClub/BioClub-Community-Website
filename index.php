<h2>How we get it done</h2>
<p>2ヶ月半で自分たちのバイオファクトリーを一緒に作り上げていきます。毎週1回、講義を行い、その様子を録画します。週2日、BioClubラボを利用し、装置の製作、実験、必要なサポートを受けることができます。</p>
<p>BioHack Academyの講義では、私たちがどのようなツールを作っているのか、ラボでどのように役立つのかを知ることができます。講師の設計を再現するか、改良するか、自分で一から装置を作るか、どちらかを選択します。どこまで洗練されたものにするかは、あなた次第です。</p>
<p>このプログラムは、ハンズオン・ブートキャンプです。すべての受講生が自分の進捗状況を記録し、お互いに学び合えるよう文書化することが義務付けられています。各講義の2週間前には、推奨材料リストとチューターのデザインへのアクセス権が配布され、アイテムを購入したり、代替品を探したりするための十分な時間が確保されます。コース終了時には、受講生とクラスメートは、様々なバイオマテリアルを成長させることができる完全装備のバイオハッカーとなり、自宅で独自に生産を続けることができるようになります。</p>
